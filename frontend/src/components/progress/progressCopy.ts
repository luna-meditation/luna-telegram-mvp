export function progressText(language: AppLanguage, key: keyof typeof progressCopy.en, replacements: Record<string, string | number> = {}) {
  let value: string = progressCopy[language][key];
  Object.entries(replacements).forEach(([name, replacement]) => {
    value = value.replace(`{${name}}`, String(replacement));
  });
  return value;
}
